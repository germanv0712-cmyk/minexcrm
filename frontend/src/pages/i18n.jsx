const LangContext = React.createContext({ lang: 'es', setLang: () => {}, t: (k) => k });

const LangProvider = ({ children }) => {
  const [lang, setLang] = React.useState(() => localStorage.getItem('mx_lang') || 'es');
  React.useEffect(() => { localStorage.setItem('mx_lang', lang); document.documentElement.lang = lang; }, [lang]);
  const t = React.useCallback((k) => (dict[lang] && dict[lang][k]) || (dict.es[k] || k), [lang]);
  return <LangContext.Provider value={{ lang, setLang, t }}>{children}</LangContext.Provider>;
};

const useT = () => React.useContext(LangContext);

window.LangContext = LangContext;
window.LangProvider = LangProvider;
window.useT = useT;
